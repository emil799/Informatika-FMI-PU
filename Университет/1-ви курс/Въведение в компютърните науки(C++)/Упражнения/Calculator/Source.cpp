#include <iostream>
using namespace std;
int main() {
	double a, b;
	cout << "Enter a value for a: " << endl;
	cin >> a;
	cout << "Enter a value for b: " << endl;
	cin >> b;
	cout << "a*b="<<a * b << endl;
	cout << "a/b=" << a / b << endl;
	cout << "a+b=" << a + b << endl;
	cout << "a-b=" << a - b << endl;
}