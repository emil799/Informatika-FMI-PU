
public class Student {
	private String name;
	private int age;
	private String spec;
	
	
	
	public Student(String name, int age, String spec) {
		super();
		this.name = name;
		this.age = age;
		this.spec = spec;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public String getSpec() {
		return spec;
	}
	public void setSpec(String spec) {
		this.spec = spec;
	}
	
	
}
